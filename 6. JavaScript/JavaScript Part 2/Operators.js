// Arithmetic : +   -   *   /   %   **
let a = 5;
let b = 3;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

// Unary : ++   --
console.log(a++);
console.log(++a);
console.log(a--);
console.log(--a);

