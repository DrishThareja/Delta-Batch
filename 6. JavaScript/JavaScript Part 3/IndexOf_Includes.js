// indexOf : returns index of something
// found = index
// not found = -1

let color = ["red", "yellow", "blue"];

a = color.indexOf("red");
console.log(a);

b = color.indexOf("green");
console.log(b);

c = color.indexOf("Yellow");
console.log(c);


// includes : search for a value

d = color.includes("red");
console.log(d);

e = color.includes("green");
console.log(e);
