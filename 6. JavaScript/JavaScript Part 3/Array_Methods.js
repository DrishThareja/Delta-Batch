// Array Methods

// Push: add to end
// Pop: delete from end & returns it
// Unshift: add to start
// Shift: delete from start and returns it

let cars = ["audi", "bmw", "xuv", "maruti"];
console.log(cars);

// push
cars.push("toyota");
console.log(cars);

// pop
cars.pop();
console.log(cars);

//unshift
cars.unshift("toyota");
console.log(cars);

// shift
cars.shift();
console.log(cars);

